// Semantic Scholar API fetch
var SEMANTIC_SCHOLAR_API = 'https://api.semanticscholar.org/graph/v1/author/2101065912/papers?fields=title,year,authors,url,citationCount,venue,externalIds&limit=50';

function getManualLink(title) {
  if (!title) return '';
  var allYears = Object.keys(publicationsData);
  for (var i = 0; i < allYears.length; i++) {
    var pubs = publicationsData[allYears[i]];
    for (var j = 0; j < pubs.length; j++) {
      if (pubs[j].title && title.toLowerCase().indexOf(pubs[j].title.substring(0, 30).toLowerCase()) !== -1) {
        return pubs[j].link || '';
      }
    }
  }
  return '';
}

function fetchFromSemanticScholar() {
  return fetch(SEMANTIC_SCHOLAR_API)
    .then(function (response) {
      if (!response.ok) throw new Error('API request failed');
      return response.json();
    })
    .then(function (data) {
      if (!data.data || data.data.length === 0) throw new Error('No data returned');
      return transformApiData(data.data);
    });
}

